public class Punto {

    public double x;
    public double y;

    public Punto() {
        super();
    }

    public Punto(double px, double py) {
        this.x = px;
        this.y = py;

    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
    
    
